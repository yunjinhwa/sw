print("hi python") # " ' 는 글자 입력
print('"hi python"') #따옴표 안에 따옴표 한번 더 써서 출력문에 따옴표 입력 단 따옴표는 같은 종류는 안됨
print("\"hy python\"") #\사용하여 따옴표 사용
print("hi python"*10) #따옴표 바깥에 *사용하여 출력 횟수 지정
print('hi \
      python') # \ 사용하여 두개의 줄을 하나로 취급, 괄호가 한줄에 같이있어야됨
print('hi \n python') # \n 사용하여 줄바꿈
print('hi \t\t python') # \t 사용하여 tab만큼 띄워서 출력
print('당신의 나이는 {}세 입니다'.format(19)) #{}자리에 format()에서 지정된 숫자가 출력

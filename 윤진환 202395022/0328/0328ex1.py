num1=10 #num1 변수에 정수 10 저장
num2=5 #num2 변수에 정수 5 저장

res1=num1*num2 #res1 변수에 num1*num2 결과 저장
res2=num1/num2 #res2 변수에 num1/num2 결과 저장, 나누기 결과는 항상 실수로 나타내기 떄문에 소수점까지 표현
res3=num1//num2 #res3 변수에 num1//num2 결과 저장
res4=num1%num2 #res4 변수에 num1%num2 결과 저장

print("num1*num2=",res1)
print("num1/num2=",res2)
print("num1//num2=",res3)
print("num1%num2=",res4)